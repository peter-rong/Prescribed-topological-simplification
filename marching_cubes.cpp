int getHashDiv(int x1, int y1, int z1, int x2, int y2, int z2,
               const std::vector<std::vector<std::vector<double>>>& zeroImg,
               std::vector<Point3D>& vertices,
               std::unordered_map<EdgeCoordinates, int, EdgeCoordinatesHash>& hash) {
    
    int X1 = x1, Y1 = y1, Z1 = z1, X2 = x2, Y2 = y2, Z2 = z2;
    
    // Handle x1 == x2 && z1 == z2 case
    if (x1 == x2 && z1 == z2 && x1 % 2 == 0 && z1 % 2 == 0) {
        if (y1 % 2 == 0) {
            if ((zeroImg[x1][y2][z1] > 0 && zeroImg[x1][y2 + 1][z1] > 0) ||
                (zeroImg[x1][y2][z1] <= 0 && zeroImg[x1][y2 + 1][z1] <= 0)) {
                Y2 = y2 + 1;
            }
        }
        if (y1 % 2 == 1) {
            if ((zeroImg[x1][y1][z1] > 0 && zeroImg[x1][y1 - 1][z1] > 0) ||
                (zeroImg[x1][y1][z1] <= 0 && zeroImg[x1][y1 - 1][z1] <= 0)) {
                Y1 = y1 - 1;
            }
        }
    }
    
    // Handle y1 == y2 && z1 == z2 case
    if (y1 == y2 && z1 == z2 && y1 % 2 == 0 && z1 % 2 == 0) {
        if (x1 % 2 == 0) {
            if ((zeroImg[x2][y1][z1] > 0 && zeroImg[x2 + 1][y1][z1] > 0) ||
                (zeroImg[x2][y1][z1] <= 0 && zeroImg[x2 + 1][y1][z1] <= 0)) {
                X2 = x2 + 1;
            }
        }
        if (x1 % 2 == 1) {
            if ((zeroImg[x1][y1][z1] > 0 && zeroImg[x1 - 1][y1][z1] > 0) ||
                (zeroImg[x1][y1][z1] <= 0 && zeroImg[x1 - 1][y1][z1] <= 0)) {
                X1 = x1 - 1;
            }
        }
    }
    
    // Handle x1 == x2 && y1 == y2 case
    if (x1 == x2 && y1 == y2 && x1 % 2 == 0 && y1 % 2 == 0) {
        if (z1 % 2 == 0) {
            if ((zeroImg[x1][y1][z2] > 0 && zeroImg[x1][y1][z2 + 1] > 0) ||
                (zeroImg[x1][y1][z2] <= 0 && zeroImg[x1][y1][z2 + 1] <= 0)) {
                Z2 = z2 + 1;
            }
        }
        if (z1 % 2 == 1) {
            if ((zeroImg[x1][y1][z1] > 0 && zeroImg[x1][y1][z1 - 1] > 0) ||
                (zeroImg[x1][y1][z1] <= 0 && zeroImg[x1][y1][z1 - 1] <= 0)) {
                Z1 = z1 - 1;
            }
        }
    }
    
    EdgeCoordinates edge(X1, Y1, Z1, X2, Y2, Z2);
    auto it = hash.find(edge);
    if (it != hash.end()) {
        return it->second;
    }
    
    // Create new vertex
    Point3D vertex((X1 / 2.0 + X2 / 2.0),
                   (Y1 / 2.0 + Y2  / 2.0),
                   ((Z1 / 2.0 + Z2 / 2.0)));
    
    vertices.push_back(vertex);
    int vertexIndex = vertices.size();
    hash[edge] = vertexIndex;
    
    return vertexIndex;
}

