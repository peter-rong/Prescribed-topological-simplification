// Hash structure for 6 coordinates (x1, y1, z1, x2, y2, z2)
struct EdgeCoordinates {
    int x1, y1, z1, x2, y2, z2;
    
    EdgeCoordinates(int x1, int y1, int z1, int x2, int y2, int z2) 
        : x1(x1), y1(y1), z1(z1), x2(x2), y2(y2), z2(z2) {}
    
    bool operator==(const EdgeCoordinates& other) const {
        return (x1 == other.x1 && y1 == other.y1 && z1 == other.z1 &&
                x2 == other.x2 && y2 == other.y2 && z2 == other.z2) ||
               (x1 == other.x2 && y1 == other.y2 && z1 == other.z2 &&
                x2 == other.x1 && y2 == other.y1 && z2 == other.z1);
    }
};

// Hash function for EdgeCoordinates
struct EdgeCoordinatesHash {
    std::size_t operator()(const EdgeCoordinates& edge) const {
        // Ensure consistent ordering for hash
        int minX = std::min(edge.x1, edge.x2), maxX = std::max(edge.x1, edge.x2);
        int minY = std::min(edge.y1, edge.y2), maxY = std::max(edge.y1, edge.y2);
        int minZ = std::min(edge.z1, edge.z2), maxZ = std::max(edge.z1, edge.z2);
        
        std::size_t h1 = std::hash<int>()(minX);
        std::size_t h2 = std::hash<int>()(minY);
        std::size_t h3 = std::hash<int>()(minZ);
        std::size_t h4 = std::hash<int>()(maxX);
        std::size_t h5 = std::hash<int>()(maxY);
        std::size_t h6 = std::hash<int>()(maxZ);
        
        return h1 ^ (h2 << 1) ^ (h3 << 2) ^ (h4 << 3) ^ (h5 << 4) ^ (h6 << 5);
    }
};

inline bool sameSign(double a, double b) {
    return (a > 0.0 && b > 0.0) || (a <= 0.0 && b <= 0.0);
}

